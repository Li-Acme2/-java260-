package 由浅入深学Java_基础进阶必做260题;

public class 逻辑运算符 {
	public static void main(String[] args){
	boolean a=true;
	boolean b=false;
	System.out.println("a&b:"+(a&b));
	System.out.println("a|b:"+(a|b));
	System.out.println("!a:"+(!a));
	System.out.println("a&&b:"+(a&&b));
	System.out.println("a||b:"+(a||b));
	System.out.println("a^b:"+(a^b));
	System.out.println("a^a:"+(a^a));
	System.out.println("0.0");
	System.out.println("qwq");
	}

}
